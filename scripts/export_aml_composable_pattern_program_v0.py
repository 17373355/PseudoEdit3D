"""Export a composable AML pattern-program draft from the composition forest.

This script turns the mined motion-pattern forest into a program-facing
artifact. It does not promote nodes as final labels and does not add
case-specific rules.

Pipeline:
1. Read the composition forest generated by
   `build_hml3d_composition_pattern_forest_v0.py`.
2. Preserve the hierarchy:
   composition_root -> structure_group -> composition_family -> variant.
3. Build condition entries for structure groups and families.
4. Export a compact program JSON plus a readable tree.

Quick run:
    python scripts/export_aml_composable_pattern_program_v0.py

Smoke test:
    python scripts/export_aml_composable_pattern_program_v0.py --self-test
"""

